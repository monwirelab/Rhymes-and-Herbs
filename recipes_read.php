                            <li>50g ginger, roughly chopped</li>
                            <li>4 tbsp vegetable oil</li>
                            <li>2 tsp cumin seeds</li>
                            <li>1 tsp fennel seeds</li>
                            <li>5cm cinnamon stick</li>
                            <li>1 tsp chilli flakes</li>
                            <li>1 tsp garam masala</li>
                            <li>1 tsp turmeric</li>
                            <li>1 tsp caster sugar</li>
                            <li>400g can chopped tomatoes</li>
                            <li>8 chicken thighs, skinned, boneless (about 800g)</li>
                            <li>250ml hot chicken stock</li>
                            <li>2 tbsp chopped coriander</li>
                        </ul>
                        <ol>
                            <li>1. Roughly chop 1 large onion, transfer to a small food processor, 
                                and add 3 tbsp of water - process to a slack paste. You could use 
                                a stick blender for this or coarsely grate the onion into a bowl – 
                                there’s no need to add any water if you are grating the onion. Tip 
                                into a small bowl and leave on one side.</li>
                            <li>2. Put 6 roughly chopped garlic cloves and 50g roughly chopped ginger 
                                into the same food processor and add 4 tbsp water – process until smooth 
                                and spoon into another small bowl. Alternatively, crush the garlic to a 
                                paste with a knife or garlic press and finely grate the ginger.</li>
                            <li>3. Heat 4 tbsp vegetable oil in a wok or sturdy pan set over a medium heat.</li>
                        </ol>
